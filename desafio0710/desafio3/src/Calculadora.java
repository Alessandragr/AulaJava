public class Calculadora {

    public int soma (int a, int b) {
        int soma = a + b;
        return soma;
    }
    public double soma (double a, double b) {
        double soma = a + b;
        return soma;
    }
    public float soma (float a, float b, float c) {
        float soma = a + b + c;
        return soma;
    }
    public long soma (long a, long b, long c) {
        long soma = a + b + c;
        return soma;
    }

    
}
